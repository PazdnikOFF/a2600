#pragma once

#include <string>

// Имена клавиш (реф. KKey2Name, X-2600).
//
// РЕВЕРС: класс БЕЗ состояния — ни ctor/dtor, ни vtable, ни статических полей;
// оба метода принимают только int (+ sret). Реализовано как статические методы.
//
// ⚠️ КВИРК ОРИГИНАЛА (важен для понимания, у нас НЕ воспроизводится дословно):
// GetNameOfKey @0x776808 занимает 0x862c байт и КАЖДЫЙ ВЫЗОВ заново собирает
// на стеке (кадр 24608 байт!) массив из 614 записей {int, std::string}, после
// чего линейно ищет по нему и в конце разрушает все 614 строк. То есть каждый
// вызов — это 614 конструирований и разрушений std::string. Мы храним таблицу
// статически (данные и порядок идентичны), сохраняя НАБЛЮДАЕМОЕ поведение:
// линейный поиск с возвратом ПЕРВОГО совпадения и "SONO_Unknown" при промахе.
//
// ⚠️ НАХОДКА: массивы g_strKeysym_S40 (233) и g_strKeysym_S50 (357) в .bss,
// которые заполняет _GLOBAL__sub_I_KKey2Name.cpp, **НИКТО В БИНАРНИКЕ НЕ
// ЧИТАЕТ** — мёртвые данные, устаревшие копии того же списка имён
// (S50 короче на 24 записи). GetNameOfKey к ним не обращается.
class KKey2Name
{
public:
    // Промах → "SONO_Unknown" (.rodata @0x89e5d0, ветка 0x77d0d4).
    static std::string GetNameOfKey(int eKey);
    // Промах → "" (пустая строка).
    static std::string GetNameOfQtScancode(int nScancode);
};
